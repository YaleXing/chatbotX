def get_random_face(emotion: str = None) -> int:
    """
    获取随机表情 ID

    Args:
        emotion: 情绪类型

    Returns:
        表情 ID
    """
    import random

    if emotion and emotion in EMOTION_FACES:
        return random.choice(EMOTION_FACES[emotion])

    # 随机选择一个常用表情
    common_faces = [14, 155, 159, 148, 172, 173, 177, 179, 193, 214, 217, 218, 250, 251, 253]
    return random.choice(common_faces)


def get_face_cq(face_id: int) -> str:
    """
    获取表情 CQ 码

    Args:
        face_id: 表情 ID

    Returns:
        CQ 码
    """
    return f"[CQ:face,id={face_id}]"
